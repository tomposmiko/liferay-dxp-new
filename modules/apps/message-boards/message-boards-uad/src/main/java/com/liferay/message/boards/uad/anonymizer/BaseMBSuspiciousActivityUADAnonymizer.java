package com.liferay.message.boards.uad.anonymizer;

import com.liferay.asset.kernel.model.AssetEntry;
import com.liferay.asset.kernel.service.AssetEntryLocalService;
import com.liferay.message.boards.model.MBSuspiciousActivity;
import com.liferay.message.boards.service.MBSuspiciousActivityLocalService;
import com.liferay.message.boards.uad.constants.MBUADConstants;
import com.liferay.portal.kernel.dao.orm.ActionableDynamicQuery;
import com.liferay.portal.kernel.exception.PortalException;
import com.liferay.portal.kernel.model.User;
import com.liferay.user.associated.data.anonymizer.DynamicQueryUADAnonymizer;

import org.osgi.service.component.annotations.Reference;

/**
 * Provides the base implementation for the message boards suspicious activity UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link MBSuspiciousActivityUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseMBSuspiciousActivityUADAnonymizer
	extends DynamicQueryUADAnonymizer<MBSuspiciousActivity> {

	@Override
	public void autoAnonymize(
			MBSuspiciousActivity mbSuspiciousActivity, long userId,
			User anonymousUser)
		throws PortalException {

		if (mbSuspiciousActivity.getUserId() == userId) {
			mbSuspiciousActivity.setUserId(anonymousUser.getUserId());
			mbSuspiciousActivity.setUserName(anonymousUser.getFullName());

			autoAnonymizeAssetEntry(mbSuspiciousActivity, anonymousUser);
		}

		mbSuspiciousActivityLocalService.updateMBSuspiciousActivity(
			mbSuspiciousActivity);
	}

	@Override
	public void delete(MBSuspiciousActivity mbSuspiciousActivity)
		throws PortalException {

		mbSuspiciousActivityLocalService.deleteMBSuspiciousActivity(
			mbSuspiciousActivity);
	}

	@Override
	public Class<MBSuspiciousActivity> getTypeClass() {
		return MBSuspiciousActivity.class;
	}

	protected void autoAnonymizeAssetEntry(
		MBSuspiciousActivity mbSuspiciousActivity, User anonymousUser) {

		AssetEntry assetEntry = fetchAssetEntry(mbSuspiciousActivity);

		if (assetEntry != null) {
			assetEntry.setUserId(anonymousUser.getUserId());
			assetEntry.setUserName(anonymousUser.getFullName());

			assetEntryLocalService.updateAssetEntry(assetEntry);
		}
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return mbSuspiciousActivityLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return MBUADConstants.USER_ID_FIELD_NAMES_MB_SUSPICIOUS_ACTIVITY;
	}

	protected AssetEntry fetchAssetEntry(
		MBSuspiciousActivity mbSuspiciousActivity) {

		return assetEntryLocalService.fetchEntry(
			MBSuspiciousActivity.class.getName(),
			mbSuspiciousActivity.getSuspiciousActivityId());
	}

	@Reference
	protected AssetEntryLocalService assetEntryLocalService;

	@Reference
	protected MBSuspiciousActivityLocalService mbSuspiciousActivityLocalService;

}